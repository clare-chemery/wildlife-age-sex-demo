from typing import Literal

import numpy as np
import pandas as pd


def preprocess_data(
    data: pd.DataFrame,
    confidence_threshold: float = 0.95,
    cropping_mode: Literal["pad", "shift", None] = "shift",
    **kwargs,
) -> pd.DataFrame:
    """
    Preprocess the data.

    Args:
    -----
    data: pd.DataFrame
        The data to preprocess containing the columns:
        - 'image_id': The id of the image. Unique, used to locate image in downstream tasks.
        - 'image': The matrix representation of the image.
        - 'conf': The confidence score of the bounding box.
        - 'bbox': The relativebounding box coordinates of the animal in the image.
        - 'sex': The sex of the animal if labeled (male, female, unknown).
        - 'age': The age of the animal if labeled (yearling, adult, juvenile, unknown).
        - other metadata columns.
    NOTE: The columns names are generated by the bbox cli tool, subject to change. Provide mapping?

    confidence_threshold: float
        The confidence threshold to use for the bounding box.
        If a bounding box has a confidence score below this threshold, it is excluded from the
        dataset.

    croppping_mode: str
        If "pad", crop the image to a square and pad.
        If "shift", square and try to shift the cropping box to fit within the image bounds.
            Otherwise, pad.
        If None, return the uncropped image.

    Yields:
    -------
    tuple(pd.DataFrame, pd.DataFrame)
        A tuple of dataframes, train and test.
    """
    preprocessed_data = data[data["conf"] >= confidence_threshold].drop(columns=["conf"])

    if cropping_mode == "shift":
        preprocessed_data["image"] = preprocessed_data.apply(
            lambda row: crop_image(row["image"], row["bbox"], "shift")
            if row["image"] is not None
            else None,
            axis=1,
        )
    elif cropping_mode == "pad":
        preprocessed_data["image"] = preprocessed_data.apply(
            lambda row: crop_image(row["image"], row["bbox"], "pad")
            if row["image"] is not None
            else None,
            axis=1,
        )
    return preprocessed_data


def crop_image(
    full_image: str,
    bbox_coords: list[int, int, int, int],
    cropping_mode: Literal["shift", "pad", None] = "shift",
) -> np.ndarray:
    """
    Load and crop an image based on the bounding box coordinates. Results in a square image that
    contains the full bounding box, padded with zeros where it exceeds the image bounds.

    Args:
    -----
    image_path: str
        The absolute path to the uncropped image.
    bbox_coords: list[int, int, int, int]
        The bounding box coordinates of the animal in the image.

    Returns:
    --------
    np.ndarray
    """
    x_coords, y_coords = get_absolute_coords(bbox_coords, full_image.shape[:2])
    if cropping_mode == "shift":
        cropped_image = _crop_image(full_image, x_coords, y_coords)
    elif cropping_mode == "pad":
        cropped_image = _crop_and_pad_image(full_image, x_coords, y_coords)
    else:
        raise ValueError(f"Invalid cropping mode: {cropping_mode}. Use 'shift' or 'pad'.")
    return cropped_image


def get_absolute_coords(
    bbox_coords: list[int, int, int, int], image_shape: tuple[int, int]
) -> tuple[tuple[int, int], tuple[int, int]]:
    """
    Get the absolute coordinates of the bounding box.

    Args:
    -----
    bbox_coords: list[int, int, int, int]
        The bounding box coordinates, proportion of image width/height.
    image_shape: tuple[int, int]
        The shape of the image, (width, height).

    Returns:
    --------
    tuple[tuple[int, int], tuple[int, int]]
        The absolute coordinates of the bounding box. (x_min, x_max), (y_min, y_max)
    """
    # Convert proportional coordinates to absolute pixel values
    height, width = image_shape
    x_coords = int(bbox_coords[0] * width), int(bbox_coords[2] * width)
    y_coords = int(bbox_coords[1] * height), int(bbox_coords[3] * height)
    # Return x range, y range
    return (min(x_coords), max(x_coords)), (min(y_coords), max(y_coords))


def _crop_and_pad_image(
    image: np.ndarray, x_coords: tuple[int, int], y_coords: tuple[int, int]
) -> np.ndarray:
    """
    Crop image to square, pad with zeros if necessary.

    Args:
    -----
    image: np.ndarray
        The image to crop and pad.
    x_coords: tuple[int, int]
        The x coordinates of the bounding box.
    y_coords: tuple[int, int]
        The y coordinates of the bounding box.

    Returns:
    --------
    np.ndarray
        The cropped and padded image.
    """
    # Get image dimensions and coordinates
    height, width = image.shape[:2]

    x_min, x_max = x_coords
    y_min, y_max = y_coords

    # Calculate the size of the square crop
    crop_size = max(x_max - x_min, y_max - y_min)

    # Validate crop size
    if crop_size <= 0:
        raise ValueError(f"Invalid crop size: {crop_size}. Check bounding box coordinates.")

    # Create a zero-padded array of the desired size
    if len(image.shape) == 3:  # RBG image
        padded = np.zeros((crop_size, crop_size, image.shape[2]), dtype=image.dtype)
    else:  # Grayscale image
        padded = np.zeros((crop_size, crop_size), dtype=image.dtype)

    # Calculate valid crop region (within image bounds)
    x_start = max(0, x_min)
    x_end = min(width, x_max)
    y_start = max(0, y_min)
    y_end = min(height, y_max)

    # Calculate corresponding region in padded array
    pad_x_start = max(0, -x_min)
    pad_y_start = max(0, -y_min)

    # Copy valid portion of image to padded array
    padded[
        pad_y_start : pad_y_start + (y_end - y_start),
        pad_x_start : pad_x_start + (x_end - x_start),
    ] = image[y_start:y_end, x_start:x_end]

    return padded


def _crop_image(
    image: np.ndarray, x_coords: tuple[int, int], y_coords: tuple[int, int]
) -> np.ndarray:
    """
    Try to crop image by shifting bounding box first, fall back to padding if needed.

    Args:
    -----
    image: np.ndarray
        The image to crop.
    x_coords: tuple[int, int]
        The x coordinates of the bounding box.
    y_coords: tuple[int, int]
        The y coordinates of the bounding box.

    Returns:
    --------
    np.ndarray
        The cropped image.
    """
    # Get image dimensions and coordinates
    height, width = image.shape[:2]

    x_min, x_max = x_coords
    y_min, y_max = y_coords

    # Calculate the size of the square crop
    crop_size = max(x_max - x_min, y_max - y_min)

    # Validate crop size
    if crop_size <= 0:
        raise ValueError(f"Invalid crop size: {crop_size}. Check bounding box coordinates.")

    # Calculate approx. center of bounding box
    center_x = (x_min + x_max) // 2
    center_y = (y_min + y_max) // 2
    half_size = crop_size // 2

    # Calculate X shift
    x_shift = 0
    if center_x - half_size < 0:
        # Shift right if crop would go off left edge
        x_shift = abs(center_x - half_size)
    elif center_x + half_size > width:
        # Shift left if crop would go off right edge
        x_shift = -(center_x + half_size - width)

    # Calculate Y shift
    y_shift = 0
    if center_y - half_size < 0:
        # Shift down if crop would go off top edge
        y_shift = abs(center_y - half_size)
    elif center_y + half_size > height:
        # Shift up if crop would go off bottom edge
        y_shift = -(center_y + half_size - height)

    # Calculate crop boundaries
    x_start = max(0, center_x - half_size + x_shift)
    y_start = max(0, center_y - half_size + y_shift)

    # Check if shifted crop would work
    if x_start + crop_size <= width and y_start + crop_size <= height:
        # Shifted crop works, return it
        return image[y_start : y_start + crop_size, x_start : x_start + crop_size]
    else:
        # Fall back to original padding method
        return _crop_and_pad_image(image, x_coords, y_coords)
